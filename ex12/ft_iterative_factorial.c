int		ft_iterative_factorial(int nb)
{
	int n;

	n = nb - 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (n > 0)
	{
		nb *= n;
		n--;
	}
	return (nb);
}
